package io.github.sadellie.indexxo.core.common

inline fun <T, S, R: Comparable<R>> Map<T, S>.sortByValuesDescending(
  crossinline selector: (Map.Entry<T, S>) -> R?
): Map<T, S> = entries
  .sortedByDescending { t -> selector(t) }
  .associate { it.key to it.value }
