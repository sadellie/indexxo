@Composable
internal fun IndexxoTheme(darkTheme: Boolean = true, content: @Composable () -> Unit) {
  val colorScheme =
    when {
      darkTheme -> darkScheme
      else -> lightScheme
    }

  MaterialTheme(colorScheme = colorScheme, typography = IndexxoTypography, content = content)
}

@Composable
internal fun Previewer(darkTheme: Boolean = true, content: @Composable () -> Unit) {
  IndexxoTheme(darkTheme = darkTheme) {
    Scaffold { content() }
  }
}

@Composable
fun isDarkThemeEnabled(): Boolean =
  MaterialTheme.colorScheme.background.luminance() < DARK_LUMINANCE

private const val DARK_LUMINANCE = 0.5f
